int	ft_atoi(char *str)
{
	int a;
	int i;
	int neg;

	i = 0;
	a = 0;
	neg = 1;
	while ((*str >= 9 && *str <= 14) || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		a = (a * 10) + (str[i] - 48);
		str++;
	}
	return (a * neg);
}
